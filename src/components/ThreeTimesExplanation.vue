<p>In order to standardize time for different longitudes, <b>Coordinated Universal Time (UTC)</b> is used, which represents the mean solar time at 0 degrees longitude (or, within 1 second of it). <b>Local standard time</b> is UTC adjusted by the time zone.</p>

<p><i><b>*</b>Well, almost exactly. The length of the mean solar day actually varies a tiny bit each year compared to International Atomic Time. This sometimes requires a <b>leap second</b> be added to or subtracted from UTC.</i></p>

