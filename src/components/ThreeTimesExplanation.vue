<p style="margin-top:0"><b>Apparent solar time</b> is the time based on the observed local position of the sun. 12 noon is the exact time at which the sun is highest in the sky.</p>

<p>However, if you measure the length of time between two consecutive solar noons, the measurement could differ from 24 hours by -21 to +30 seconds. (see the <a href="https://en.wikipedia.org/wiki/Solar_time" target="_blank">Wikipedia article on Solar Time</a> for an explanation).</p>

<p>Instead, in <b>Mean solar time</b>, every day is exactly<b>*</b> 24 hours long. This is more useful, but due to those small deviations in the length of the solar day adding up, the mean solar time can differ from the apparent solar time by up to 16 minutes. The relationship between the apparent and mean solar time can be seen in the <b>Equation of Time</b>.</p>

